package net.chinanets.vo;


public class Chart {
	
	private String name;
	private double count;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setCount(double count) {
		this.count = count;
	}
	public double getCount() {
		return count;
	}

}

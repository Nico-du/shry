package net.chinanets.vo;

public class RuleVo {

	private Long id;
	private String mc;
	private String bz;
	private String yxj;
	private String bmid;
	private String bmmc;
	private String kmcz;

	public RuleVo() {

	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getMc() {
		return mc;
	}

	public void setMc(String mc) {
		this.mc = mc;
	}

	public String getBz() {
		return bz;
	}

	public void setBz(String bz) {
		this.bz = bz;
	}

	public String getYxj() {
		return yxj;
	}

	public void setYxj(String yxj) {
		this.yxj = yxj;
	}

	public String getBmid() {
		return bmid;
	}

	public void setBmid(String bmid) {
		this.bmid = bmid;
	}

	public String getBmmc() {
		return bmmc;
	}

	public void setBmmc(String bmmc) {
		this.bmmc = bmmc;
	}

	public String getKmcz() {
		return kmcz;
	}

	public void setKmcz(String kmcz) {
		this.kmcz = kmcz;
	}

}
